    <div class="section method" ria-labelledby="method-title">
        <h2>Quy trình</h2>
        <div class="step">
        <strong>Bước 1</strong>
            <br>– Cho 350 g bông cải xanh, 150 g đậu tằm, 2 tép tỏi, ½ củ hành tây và 1 nắm húng lủi  vào máy xay thực phẩm. Nhấn, nhồi cho đến khi nguyên liệu được băm nhuyễn.
            <br>– Trong một tô lớn, trộn đều 1 chén (khoảng 125 g) bột mì, 1 muỗng cà phê bột nở, 1 muỗng cà phê thì là xay và 1 muỗng cà phê hạt ngò xay. Nêm ½ muỗng cà phê muối và chút tiêu.
            <br>– Tạo một “giếng” ở giữa hỗn hợp bột, đập vào 1 quả trứng và đổ ⅔ chén (≈160 ml) nước. Dùng phới lồng đánh đến khi bột mịn, không vón cục.
            <br>– Cho hỗn hợp bông cải đã xay vào tô bột, trộn đều cho đến khi quyện hẳn.
        </div>
        <div class="step">
        <strong>Bước 2</strong>
            <br>– Đun nóng dầu ăn trong chảo chống dính ở lửa vừa.
            <br>– Múc mỗi lần ¼ chén (≈60 ml) hỗn hợp bột cho lên chảo, nhẹ nhàng nén dẹt thành miếng tròn đường kính khoảng 6 cm. Làm lần lượt 3 miếng rồi rán mỗi mặt khoảng 2 phút hoặc đến khi vàng đều và chín.
            <br>– Vớt ra giấy thấm dầu. Tiếp tục rán phần bột còn lại để được tổng cộng 12 chiếc fritters.
        </div>
        <div class="step">
        <strong>Bước 3</strong><br>
            – Trong khi chờ, làm “sữa chua xanh”: cho 200 g sữa chua không đường, 1 nắm húng lủi và 1 muỗng canh nước cốt chanh vào máy xay nhỏ. Xay đến khi hỗn hợp mịn, có màu xanh nhạt. Nêm ¼ muỗng cà phê muối và chút tiêu cho vừa ăn.
        </div>
        <div class="step">
        <strong>Bước 4</strong><br>
            – Bày bánh bông cải xanh ra đĩa, rưới hoặc chấm kèm sữa chua xanh. Thêm dưa leo thái lát, roti (hoặc bánh mì), và rau salad theo ý thích. Trang trí thêm lá húng tươi trước khi thưởng thức.
        </div>
    </div>
